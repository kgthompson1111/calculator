/* addition, subtraction, multiplication and division functions */

function add(x, y) {
    return (x + y);
}

function subtract(x, y) {
    return (x - y);
}

function multiply(x, y) {
    return (x * y);
}

function divide(x, y) {
    return (x / y);
}

function operate(x, y) {
    return add(x, y);
}